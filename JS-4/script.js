for (let i = 1; i <= 10; i++) {
    console.log(`Number: ${i}`);
}

let hobbies = [
    "Running",
    "Drawing",
    "Designing",
    "Gaming",
    "Studying"
];

let index = 0;
while (index < hobbies.length) {
    console.log(`Hobby ${index + 1}: ${hobbies[index]}`);
    index++;
}